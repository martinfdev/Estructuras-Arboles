    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!"".equals(txtCarne.getText()) && !"".equals(txtApellido.getText()) && !"".equals(txtCarrera.getText())
                && !"".equals(txtNombre.getText()) && !"".equals(txtPassword.getText())) {
            encripatacion = new Encrypted();
            int carne = Integer.parseInt(txtCarne.getText());
            String password = encripatacion.getMD5(txtPassword.getText());
            tableuser.insert(new User(carne, txtNombre.getText(), txtApellido.getText(), txtCarrera.getText(), password), carne);
            clearTxt();
            JOptionPane.showMessageDialog(null, "Registrado!");
            if(registroLogin)
                formWindowClosing(null);
        } else
            JOptionPane.showMessageDialog(null, "No dejar campo vacio!");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnBuscarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarArchivoActionPerformed
        ReadJson rj = new ReadJson(tableuser);
        File f;
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        file.setFileSelectionMode(JFileChooser.FILES_ONLY);
        file.addChoosableFileFilter(new FileNameExtensionFilter("Archivo tipo json", "json"));
        file.setAcceptAllFileFilterUsed(true);
        int option = file.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
             f = file.getSelectedFile();
             String path = file.getSelectedFile().getPath();
             rj.readJsonUser(path);
        }  
    }//GEN-LAST:event_btnBuscarArchivoActionPerformed

