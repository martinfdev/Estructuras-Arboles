    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int op = JOptionPane.showConfirmDialog(null, "Desea eliminar su cuenta?");
        if (op==0) {
          int carne = Integer.parseInt(txtCarne.getText());
          boolean deleted = tableuser.delete(carne);
            if (deleted) {
                JOptionPane.showMessageDialog(null, "Cuenta borrada");
                disableC();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void setDataForm() {
        if (active_user != null) {
            txtApellido.setText(active_user.getApellido());
            txtCarne.setText(String.valueOf(active_user.getNumero_carne()));
            txtCarrera.setText(active_user.getCarrera());
            txtNombre.setText(active_user.getNombre());
            txtPassword.setText(windwowmain.password);
        }
    }

    private void disableC() {
        txtApellido.setEnabled(false);
        txtCarne.setEnabled(false);
        txtCarrera.setEnabled(false);
        txtNombre.setEnabled(false);
        txtPassword.setEnabled(false);
    }

    private void enableC() {
        txtApellido.setEnabled(true);
        txtCarne.setEnabled(true);
        txtCarrera.setEnabled(true);
        txtNombre.setEnabled(true);
        txtPassword.setEnabled(true);
    }
    

