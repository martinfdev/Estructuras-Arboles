    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int carne = Integer.parseInt(txtCarne.getText());
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String carrera = txtCarrera.getText();
        String pass = encriptacion.getMD5(txtPassword.getText());
        if (carne>0) {
            tableuser.search(carne).setApellido(apellido);
            tableuser.search(carne).setCarrera(carrera);
            tableuser.search(carne).setNombre(nombre);
            tableuser.search(carne).setNumero_carne(carne);
            tableuser.search(carne).setPassword(pass);
            JOptionPane.showMessageDialog(null, "Cambios Guardados!");
            disableC();
        }
    
    }//GEN-LAST:event_btnGuardarActionPerformed

